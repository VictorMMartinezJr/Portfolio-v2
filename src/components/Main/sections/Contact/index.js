    e.preventDefault();
    setSending(true);

    // Validate Email
    if (!validateEmail(email)) {
      setEmailError(true);
      setSending(false);
      return;
    }
    // Validate Name
    if (!validateTextInputs(name)) {
      setNameError(true);
      setSending(false);
      return;
    }
    // Validate Message
    if (!validateTextInputs(message)) {
      setMessageError(true);
      setSending(false);
      return;
    }

